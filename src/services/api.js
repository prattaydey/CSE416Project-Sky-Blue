const API_BASE_URL = (
  import.meta.env.VITE_API_BASE_URL ||
  import.meta.env.VITE_BACKEND_URL ||
  "http://localhost:3000"
).replace(/\/+$/, "");

const CLIENT_BACKEND_URL = (
  import.meta.env.VITE_CLIENT_BACKEND_URL ||
  API_BASE_URL
).replace(/\/+$/, "");

const APP_CLIENT_KEY = import.meta.env.VITE_APP_CLIENT_KEY || "";

function getAuthHeaders() {
  if (!APP_CLIENT_KEY) {
    throw new Error("Missing VITE_APP_CLIENT_KEY in frontend .env");
  }

  return {
    Authorization: `Bearer ${APP_CLIENT_KEY}`,
  };
}

function getUserAuthHeaders() {
  const token = localStorage.getItem("authToken");
  if (!token) {
    return {};
  }
  return {
    Authorization: `Bearer ${token}`,
  };
}

/* GENERATED BY AI - request JSON */
async function requestJson(baseUrl, path, options = {}) {
  const {
    method = "GET",
    body,
    auth = true,
    userAuth = false,
    statusMessages = {},
  } = options;

  const headers = {
    ...(userAuth ? getUserAuthHeaders() : auth ? getAuthHeaders() : {}),
    ...(body ? { "Content-Type": "application/json" } : {}),
  };

  const res = await fetch(`${baseUrl}${path}`, {
    method,
    headers,
    body: body ? JSON.stringify(body) : undefined,
  });

  if (!res.ok) {
    if (statusMessages[res.status]) throw new Error(statusMessages[res.status]);
    throw new Error(`Server error (${res.status})`);
  }

  return res.json();
}

