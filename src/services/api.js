export async function verifyToken(token) {
  const res = await fetch(`${CLIENT_BACKEND_URL}/api/users/verify-token`, {
    method: "POST",
    headers: {
      Authorization: `Bearer ${token}`,
      "Content-Type": "application/json",
    },
    body: JSON.stringify({}),
  });

  if (!res.ok) {
    throw new Error("Invalid or expired token.");
  }

  return res.json();
}

